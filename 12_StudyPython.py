# Python List
# To store multiple items in a single variable
# List ---> with square brackets
list1 = ["axe","sword","shield"]
print(list1)

# List items --> ordered, changeable, and allow duplicate values.
# First item --> index 0

# Ordered --> the items have a defined order, that order will not change.
# If new item is added --> will be placed at the end of the list.

# Changeable --> can change, add, and remove items in a list
# Allow duplicates --> can be duplicated

# Find list length
# with len()
print(len(list1))

# List items can be any data type
list2 = ["abc", 42, True, 69.69]
print(type(list2))

# List() constructor
list3 = "Hello World"

print(list(list3))