# Python Variables
# Use variables to assign value into something.
# Python has no command for declaring a variable.

x = 5
y = "Hello World"

# When naming variable, function, class, module, or other object --> we use Python Identifiers

# Identifier starts with a letter A to Z or a to z or an underscore followed by zero or more letters, underscores, and digits.

# Punctuation characters such as @, $, and % within identifiers are not allowed.

# Python is case sensitive.

myName = "Yos"
MyName = "Mabro"

# Naming conventions
# Class names start with an uppercase letter. All other identifiers start with lowercase letter.
# Starting an identifier with a single leading underscore indicates that the identifier is private.
# Starting an identifier with two leading underscores indicates a strongly private identifier
# If the identifier also ends with two trailing underscores, the identifier is a language-defined special name.